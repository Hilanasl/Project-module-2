$(function () {
    var $arr_1 = $('.arr_1');
    $('.arr_1_marker').hover(function () {
        var t = this.getBoundingClientRect().top,
            l = this.getBoundingClientRect().left;
        $arr_1.css({ "top": t + "px", "left": l + "px" }).show();
    }, function () {
        $arr_1.hide();
    });
});

